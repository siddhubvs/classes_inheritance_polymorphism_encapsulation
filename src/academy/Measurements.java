package academy;

public class Measurements {
    private int width;
    private int height;
    private int thickness;

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getThickness() {
        return thickness;
    }

    public Measurements(int width, int height, int thickness) {
        this.width = width;
        this.height = height;
        this.thickness = thickness;

    }
}
